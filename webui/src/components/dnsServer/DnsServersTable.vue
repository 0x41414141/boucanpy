<template id="">
    <div class v-if="isAuthenticated">
        <b-table
            v-if="items.length > 0"
            striped
            hover
            :items="items"
            :fields="fields"
            :sort-by.sync="query.sort_by"
            :sort-desc="query.sort_dir == 'desc'"
            v-on:sort-changed="changeSort"
            :busy="isLoading || !isLoaded"
        >
            <template slot="actions" slot-scope="row">
                <b-button-group>
                    <router-link
                        :to="{ name: 'dns-server.edit', params: { dnsServerId: row.item.id } }"
                        tag="button"
                        class="btn btn-info btn-sm"
                    >Edit</router-link>
                    <router-link
                        :to="{ name: 'dns-server.show', params: { dnsServerId: row.item.id } }"
                        tag="button"
                        class="btn btn-info btn-sm"
                    >View</router-link>
                </b-button-group>
            </template>
        </b-table>
        <div class="col-xs-12 text-center" v-if="items.length < 1 && isLoaded">
            <span class="text-center">No Data Found :(</span>
        </div>

        <div class="col-xs-12 text-center" v-if="items.length < 1 && !isLoaded">
            <span class="text-center">Loading Data</span>
        </div>

        <b-pagination
            v-if="query.page > 0 && items.length > 0"
            v-model="query.page"
            :total-rows="total"
            :per-page="query.per_page"
            aria-controls="my-table"
            @change="changePage"
            style="margin-top:10px;"
        ></b-pagination>
    </div>
</template>

<script>
import { Vue, Component } from 'vue-property-decorator';
import { mixins } from 'vue-class-component';
import CommonMixin from '@/mixins/common';
import DnsServerMixin from '@/mixins/dnsServer';
import DataTableMixin from '@/mixins/dataTable';
import dnsServer from '@/services/dnsServer';
import bus from '@/bus';
import { GeneralQS } from '@/queries';

// TODO: move to vuex / persistent data
@Component
export default class DnsServersTable extends mixins(
    CommonMixin,
    DnsServerMixin,
    DataTableMixin,
) {
    items = [];
    query = new GeneralQS();
    isLoading = false;
    isLoaded = false;
    fields = [
        {
            key: 'id',
            label: 'ID',
            sortable: true,
        },
        {
            key: 'name',
            label: 'Name',
            sortable: true,
        },
        {
            key: 'actions',
            label: 'Actions',
        },
    ];

    changeSort(sort) {
        this.query.sort_by = sort.sortBy;
        if (sort.sortDesc) {
            this.query.sort_dir = 'desc';
        } else {
            this.query.sort_dir = 'asc';
        }
        this.loadData();
    }

    loadData() {
        this.isLoading = true;
        return dnsServer.getDnsServers(this.query).then((res) => {
            let query = new GeneralQS();
            query.page = res.pagination.page;
            query.per_page = res.pagination.per_page;
            query.sort_by = this.query.sort_by;
            query.sort_dir = this.query.sort_dir;
            this.query = query;
            this.total = res.pagination.total;
            this.items = res.dns_servers;
            this.isLoaded = true;
            this.isLoading = false;
        });
    }

    freshLoad() {
        this.loadData();
    }

    mounted() {
        this.freshLoad();
        bus.$on('DNS_SERVER_ADDED', (payload) => {
            this.freshLoad();
        });
    }
}
</script>
